package main;

public class zipcode {
    private int left;
    private int right;

    public zipcode(int left, int right) {
        this.left = left;
        this.right = right;
    }

    public int getleft() {
        return left;
    }

    public void setleft(int left) {
        this.left = left;
    }

    public int getright() {
        return right;
    }

    public void setright(int right) {
        this.right = right;
    }
}
